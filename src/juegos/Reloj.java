package juegos;

public class Reloj {

    int horas;
    int minutos;
    int segundos;

    public Reloj(){
        this.horas = 0;
        this.minutos = 0;
        this.segundos = 0;
    }
    public Reloj(int horas, int minutos, int segundos){
        this.horas = horas;
        this.minutos = minutos;
        this.segundos = segundos;
    }

    


    
}
